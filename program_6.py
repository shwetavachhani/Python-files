a=[1,5,9,11,2,66]
print('a[1:]=>',a[1:])
#a[1:]=> [5, 9, 11, 2, 66]
print('a[:1]=>',a[:1])
#a[:1]=> [1]
print('a[-1:]=>',a[-1:])
#a[-1:]=> [66]
print('a[:-1]=>',a[:-1])
#a[:-1]=> [1, 5, 9, 11, 2]
