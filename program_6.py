#a[3:]=> [11, 2, 66]
print('a[:3]=>',a[:3])
#a[:3]=> [1, 5, 9]
print('a[-3:]=>',a[-3:])
#a[-3:]=> [11, 2, 66]
print('a[:-3]=>',a[:-3])
#a[:-3]=> [1, 5, 9]
print('a[::1]=>',a[::1])
#a[::1]=> [1, 5, 9, 11, 2, 66]
print('a[::-1]=>',a[::-1])
#a[::-1]=> [66, 2, 11, 9, 5, 1]
print('a[1::]=>',a[1::])
#a[1::]=> [5, 9, 11, 2, 66]
print('a[::-1]=>',a[::-1])
#a[::-1]=> [66, 2, 11, 9, 5, 1]
print('a[::-2]=>',a[::-2])
#a[::-2]=> [66, 11, 5]
print('a[2::]=>',a[2::])
#a[2::]=> [9, 11, 2, 66]



